//delayMs(1);                      /* aguarda estabilizar o clock */
//for (i=0; i<1000; i++){};
	
if(dc) { HAL_GPIO_WritePin(GPIOE, DC_PIN_Pin, GPIO_PIN_SET);} // DC = 1 (DATA)
       else HAL_GPIO_WritePin(GPIOE, DC_PIN_Pin, GPIO_PIN_RESET); // DC = 0 (COMMAND)

while(!(SPI1->SR & SPI_SR_TXE)){}; // Aguarda até o Buffer de TX estiver vazio
SPI1->DR = data; // TX character
	
